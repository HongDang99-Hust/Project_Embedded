// Hàm callback ngắt Timer (Chạy mỗi 5 giây do cài đặt TIM1)
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM1)
  {
      // Tính thời gian
      float current_seconds = (float)(htim->Instance->ARR + 1) / 10000.0f;

      char msg[32];
      // sprintf cần thư viện <stdio.h>
      sprintf(msg, " Ngat moi %.1fs\r\n", current_seconds);
      UART_SendString(&huart2, msg);

      flag_5s = 1; // Bật cờ để main loop xử lý
  }
}

// Task 1: Đọc cảm biến DHT22
void Task_ReadDHT(void) {
    dht_ok = DHT22_Read(&temp, &humi);
}

// Task 2: Hiển thị lên LCD
void Task_LCD_Display(void) {
    if (dht_ok) {
        LCD_PrintFloat(0, 0, "Tem: ", temp, "C");
        LCD_PrintFloat(1, 0, "Hum: ", humi, "%");
    } else {
        LCD_Clear();
        LCD_Set_Cursor(0, 0);
        LCD_Send_String("DHT Error");
    }
}

// Task 3: Gửi dữ liệu qua UART
void Task_UART_Send(void) {
    char msg[64];
    if (dht_ok) {
        sprintf(msg, "Temp: %.1f | Humi: %.1f \r\n", temp, humi);
    } else {
        sprintf(msg, "DHT22 Read Failed\r\n");
    }
    UART_SendString(&huart2, msg);
}

// Hàm điều phối các công việc
void App_ExecutePeriodicTasks(void)
{
    Task_ReadDHT();
    Task_LCD_Display();
    Task_UART_Send();
}
