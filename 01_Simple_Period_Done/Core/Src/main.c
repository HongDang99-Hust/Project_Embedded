/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C2_Init();
  MX_TIM4_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  // MX_TIM3_Init(); // Đã xóa

  /* USER CODE BEGIN 2 */
  // Khởi động Timer ngắt thời gian (TIM1)
  HAL_TIM_Base_Start_IT(&htim1);

  // Khởi động PWM (TIM2)
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);

  // Khởi động UART nhận ngắt
  UART_StartReceive_IT();

  // Khởi động Driver
  DHT22_Init(&htim4);
  LCD_Init(&hi2c2);
  /* USER CODE END 2 */

  /* Infinite loop */
  while (1)
  {
      // Kiểm tra cờ ngắt thời gian (5s một lần)
      if (flag_5s)
      {
          flag_5s = 0; // Xóa cờ
          App_ExecutePeriodicTasks();  // Thực hiện đọc DHT, hiển thị LCD, gửi UART
      }
  }
}

